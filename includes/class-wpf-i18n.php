	public function load_textdomain()
	{
		load_plugin_textdomain('wp-folders', false, dirname(plugin_basename(WPF_PLUGIN_FILE)) . '/languages');
	}

	public function translate($text)
	{
		$translated = __($text, 'wp-folders');
		if ($translated !== $text) {
			return $translated;
		}

		$locale = determine_locale();
		if (0 === strpos($locale, 'nb_') || 0 === strpos($locale, 'nn_') || 0 === strpos($locale, 'no_')) {
			return $this->nb_translations[$text] ?? $text;
		}

		return $text;
	}
